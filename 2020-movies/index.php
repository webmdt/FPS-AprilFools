function getMovies($moviesArray){
	$movies = [];
	$ratings = ['R','PG13','PG','G','NC17'];
	$links = [
		'https://www.youtube.com/watch?v=XCPj4JPbKtA',
	];
	
	foreach($moviesArray as $movie){
		
		$randomDate = mt_rand(1585602155,1591747200);
		$randomRating = array_rand($ratings,1);
		$randomLink = array_rand($links,1);
		
		$movies[] = [
			'title'   => $movie['title'],
			'cover'   => $movie['cover'],
			'link'    => $links[$randomLink],
			'expireDateTime' => date("D d M - H:i",$randomDate),
			'length'  => rand(72,145) . ' mins',
			'rating'  => $ratings[$randomRating],
		];
	}

	return $movies;	
}
